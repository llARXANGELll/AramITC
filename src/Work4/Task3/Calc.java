package Work4.Task3;

public class Calc {

    public static double umn(int x, int y) {
        return x * y;
    }

    public static int del(int x, int y) {
        return x / y;
    }

    public static double sl(int x, int y) {
        return x + y;
    }

    public static double otn(int x, int y) {
        return x - y;
    }

    public static int proc(int x, int y) {
        return (x * y) / 100;
    }
}
